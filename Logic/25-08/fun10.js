function ordinario(numero) {
    if (numero === 1) {
        return "Primeiro"
    }else if (numero === 2) {
        return "Segundo"
    }else if (numero === 3) {
        return "Terceiro"
    }else if (numero === 4) {
        return "Quarto"
    }else if (numero === 5) {
        return "Quinto"
    }else if (numero === 6) {
        return "Sexto"
    }else if (numero === 7) {
        return "Sétimo"
    }else if (numero === 8) {
        return "Oitavo"
    }else if (numero === 9) {
        return "Nono"
    }else if (numero === 10) {
        return "Décimo"
    }else if (numero === 11) {
        return "Décimo primeiro"
    }else if (numero === 12) {
        return "Décimo segundo"
    }else{
        return ""
    }
}
console.log(ordinario(10));